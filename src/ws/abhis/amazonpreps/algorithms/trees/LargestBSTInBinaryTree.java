/*
 * Find the largest BST in binary tree.
 */

package ws.abhis.amazonpreps.algorithms.trees;

public class LargestBSTInBinaryTree {
	public class Node {
		public int value;
		public Node left;
		public Node right;

		public Node(int value, Node left, Node right) {
			this.value = value;
			this.left = left;
			this.right = right;
		}

	}

}
